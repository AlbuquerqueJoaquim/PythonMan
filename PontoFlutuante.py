#ponto flutuante
num_int = 5
num_dec = 5.3
val_str = "qualquer texto"

print("O valor é:", num_int)
print("O valor é: %i" %num_int)
print("O valor é: " + str(num_int))

print("Concatendo decimal:", num_dec)
